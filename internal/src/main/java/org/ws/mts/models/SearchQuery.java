package org.ws.mts.models;

public class SearchQuery {
	private String namePart;
	private String surnamePart;
	private String phonePart;
	
	public String getNamePart() {
		return namePart;
	}
	
	public void setNamePart(String namePart) {
		this.namePart = namePart;
	}
	
	public String getSurnamePart() {
		return surnamePart;
	}
	
	public void setSurnamePart(String surnamePart) {
		this.surnamePart = surnamePart;
	}
	
	public String getPhonePart() {
		return phonePart;
	}
	
	public void setPhonePart(String phonePart) {
		this.phonePart = phonePart;
	}
		
}
